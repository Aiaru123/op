# Пример использования каждой арифметической операции в Python

# 1. Вычитание
a = 10
b = 3
result_subtraction = a - b  # 10 - 3 = 7

# 2. Сложение
result_addition = a + b  # 10 + 3 = 13

# 3. Умножение
result_multiplication = a * b  # 10 * 3 = 30

# 4. Возведение в степень
result_exponentiation = a ** b  # 10 ^ 3 = 1000

# 5. Деление
result_division = a / b  # 10 / 3 = 3.333...

# 6. Целочисленное деление
result_integer_division = a // b  # 10 // 3 = 3

# 7. Остаток от деления
result_modulo = a % b  # 10 % 3 = 1

print(f"Вычитание: {result_subtraction}")
print(f"Сложение: {result_addition}")
print(f"Умножение: {result_multiplication}")
print(f"Возведение в степень: {result_exponentiation}")
print(f"Деление: {result_division}")
print(f"Целочисленное деление: {result_integer_division}")
print(f"Остаток от деления: {result_modulo}")